RANDOM_PANTUN = [
    "Percintaan",
    "Bijak",
    "Masa Depan",
    "Motivasi",
    "Jenaka",
    "Nasihat",
    "Agama",
    "Random",
]


def gen_pantun(prompt):
    genai.configure(api_key=API_GEMINI)
    aman = {
        "HATE": "BLOCK_NONE",
        "HARASSMENT": "BLOCK_NONE",
        "SEX": "BLOCK_NONE",
        "DANGER": "BLOCK_NONE",
    }

    model = genai.GenerativeModel(
        "models/gemini-1.5-flash",
        system_instruction=(
            """
sesuaikan dengan input yang anda terima.
pastikan output tidak lebih dari 1.
"""
        ),
    )

    try:
        response = model.generate_content(f"{prompt}", safety_settings=aman)
        return f"{response.text}"
    except Exception:
        return None


@CMD.UBOT("pantun|tebakan|lelucon|puisi|quotes")
async def _(client, message):
    emo = Emoji(client)
    await emo.get()
    cmd = message.command[0].lower()
    input_ = client.get_text(message).lower()

    if not input_:
        input_ = random.choice(RANDOM_PANTUN).lower()

    pros = await message.reply(
        f"{emo.proses}<b>Processing to create {cmd} {input_} ..</b>"
    )
    prompt = f"{cmd} {input_}"
    text = gen_pantun(prompt)

    if not text:
        return await pros.edit(f"{emo.gagal}<b>Failed to create {cmd} {input_}.</b>")

    return await pros.edit(
        f"{emo.sukses}<b>Successfully generated {cmd} {input_}:\n\n<code>{text}</code>\n{emo.profil} Generated by: {bot.me.mention}</b>"
    )
